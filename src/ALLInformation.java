public class ALLInformation {

    private String guess ;
    private int bulls ;
    private int cows ;

    public String getGuess() {
        return guess;
    }
    public void setGuess(String guess) {
        this.guess = guess;
    }
    public int getBulls() {
        return bulls;
    }
    public void setBulls(int bulls) {
        this.bulls = bulls;
    }
    public int getCows() {
        return cows;
    }
    public void setCows(int cows) {
        this.cows = cows;
    }

}
